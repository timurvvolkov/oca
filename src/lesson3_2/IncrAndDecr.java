package lesson3_2;

public class IncrAndDecr {
    public static void main(String[] args) {
        int i = 0;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(--i);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);
    }
}
